# break statement with for loop

for letter in 'Tafhim':     
   if letter == 'h':
      break
   print ('Current Letter :', letter)  
   # print ("Current Letter : {}".format(letter))



# break statement with while loop 
i = 11                   
while i > 0:              
   print ('Current variable value :', i)
   i = i -1
   if i == 5:
      break
print ("Stopped !!")




